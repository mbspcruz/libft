#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	while (s[i] != '\0')
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	if (s[i] != c && s[i] == '\0')
		return (NULL);
}
